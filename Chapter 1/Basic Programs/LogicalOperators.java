/*Write a program demonstrate Logical operators.*/
class LogicalOperators
{
	public static void main(String[] args) 
	{
		boolean a=true,b=false; 
		System.out.println("AND="+(a&&b));
		System.out.println("OR="+(a||b));
		System.out.println("NOT="+(!a));
	}
}
