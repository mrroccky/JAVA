import java.applet.*;
import java.awt.*;
public class SetBackGroundMethodScrollbarClass extends Applet
{
	Scrollbar Scrollbar1;
	public void init()
	{
		Scrollbar1=new Scrollbar(Scrollbar.VERTICAL,0,10,0,100);
		Scrollbar1.setBackground(Color.RED);
		add(Scrollbar1);
		 
	}
}
